<?php class Operaciones{
    public function sumar($a,$b){
        $suma=$a+$b;
        return $suma;
    }
    public function restar($a,$b){
        $resta=$a-$b;
        return $resta;
    }
    public function multiplicacion($a,$b){
        $multiplicacion=$a*$b;
        return $multiplicacion;
    }
    public function division($a,$b){
        $division=$a/$b;
        return $division;
    }
}